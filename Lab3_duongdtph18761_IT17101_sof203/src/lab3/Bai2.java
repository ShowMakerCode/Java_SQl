    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        ImageIcon icon = new ImageIcon("src/icon/Delete.png");
        int xacNhan = JOptionPane.showConfirmDialog(this, "Bạn có muốn thoát không?", "Exit", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, icon);
        if(xacNhan == JOptionPane.NO_OPTION){
            return;
        }else if(xacNhan == JOptionPane.CANCEL_OPTION){
            return;
        }else{
            System.exit(0);
        }
    }//GEN-LAST:event_btnExitActionPerformed

