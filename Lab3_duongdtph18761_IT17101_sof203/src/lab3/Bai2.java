    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String ho = this.txtHo.getText(),
                ten = this.txtTen.getText(),
                biDanh = this.txtBidanh.getText(),
                matMa =new String(this.txtPass.getPassword()),
                queQuan = this.cbbQuequan.getSelectedItem().toString(),
                memo = this.txtmemo.getText();
        String gioiTinh;
        if(this.rdoNam.isSelected()){
            gioiTinh = "Nam";
        }else if(this.rdoNu.isSelected()){
            gioiTinh = "Nữ";
        }else{
            gioiTinh = "Không Xác Định";
        }
        String hopy = "" ;
        if(this.chkAnchoi.isSelected()){
            hopy = hopy == ""? "Ăn chơi" : hopy + ", Ăn chơi";
        }
        if(this.chkNgunghi.isSelected()){
            hopy = hopy == ""? "Ngủ nghỉ" : hopy + ", Ngủ nghỉ";
        }
        if(this.chkNhaymua.isSelected()){
            hopy = hopy == ""? "Nhảy múa" : hopy + ", Nhảy múa";
        }
        String tt = "Thông Tin Sinh Viên: " + "\nHọ và tên: "+ ho + " " + ten + "\nQuê quán: " +
                queQuan +"\nGiới tính: "+ gioiTinh+"\nSở thích: "+hopy ;
         ImageIcon  img = new ImageIcon("src/icon/Bell.png");
        JOptionPane.showMessageDialog(this, tt, "Thông Tin", JOptionPane.INFORMATION_MESSAGE, img);
    }//GEN-LAST:event_btnOKActionPerformed

