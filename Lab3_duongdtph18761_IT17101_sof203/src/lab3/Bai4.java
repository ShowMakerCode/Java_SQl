    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!this.checkForm()) {
            return;
        }
        String firstName = this.txtFirstName.getText(),
                lastName = this.txtLastName.getText(),
                contact = this.txtcontact.getText(),
                addres = this.txtAddress.getText(),
                hQ = this.txtHQ.getText(),
                sF = this.txtSF.getText(),
                eF = this.txtEF.getText(),
                hobbi = this.txtHobi.getText(),
                sp = this.txtSport.getText();
        ImageIcon icon = new ImageIcon("src/icon/Print.png");
        String result = "Imformation"
                + "\nFisrt Name: " + firstName
                + "\nLast Name: " + lastName
                + "\nContact: " + contact
                + "\nAddress: " + addres
                + "\nHighest Qualification: " + hQ
                + "\nSprcifition: " + sF
                + "\nEnrol For: " + eF
                + "\nHobbies: " + hobbi
                + "\nSport: " + sp;
        JOptionPane.showMessageDialog(this, result, "Thông Tin ", JOptionPane.INFORMATION_MESSAGE, icon);
    }//GEN-LAST:event_btnSaveActionPerformed

