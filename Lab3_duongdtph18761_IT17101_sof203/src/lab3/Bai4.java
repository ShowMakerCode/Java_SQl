    public boolean checkForm() {
        if (this.txtFirstName.getText().length() == 0) {
            this.txtFirstName.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống họ", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtFirstName.setBackground(Color.WHITE);
        }
        if (this.txtLastName.getText().length() == 0) {
            this.txtLastName.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống tên", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtLastName.setBackground(Color.WHITE);
        }
        if (this.txtcontact.getText().length() == 0) {
            this.txtcontact.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống contact", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtcontact.setBackground(Color.WHITE);
        }
        if (this.txtAddress.getText().length() == 0) {
            this.txtAddress.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống Address", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtAddress.setBackground(Color.WHITE);
        }
        if (this.txtHQ.getText().length() == 0) {
            this.txtHQ.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống Highest Qualification", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtHQ.setBackground(Color.WHITE);
        }
        if (this.txtSF.getText().length() == 0) {
            this.txtSF.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống Specifition", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtSF.setBackground(Color.WHITE);
        }
        if (this.txtEF.getText().length() == 0) {
            this.txtEF.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống Enroll For", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtEF.setBackground(Color.WHITE);
        }
        if (this.txtHobi.getText().length() == 0) {
            this.txtHobi.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống Sở thích", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtHobi.setBackground(Color.WHITE);
        }
        if (this.txtSport.getText().length() == 0) {
            this.txtSport.setBackground(Color.YELLOW);
            JOptionPane.showMessageDialog(this, "Không được để trống thể thao", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            this.txtSport.setBackground(Color.WHITE);
        }
        return true;
    }

