    private void LoadDatatoList() {
        try {
            String sql = "Select * From HOCSINH";
            Statement stm = cn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            while (rs.next()) {
                listHocSinh.add(new HocSinh(rs.getString(1), rs.getString(2), rs.getString(3), rs.getDouble(4)));
            }
            stm.close();
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void showDetail() {
        HocSinh hs = listHocSinh.get(index);
        txtTen.setText(hs.getTenHS());
        txtMAHS.setText(hs.getMaHS());
        txtDiemTb.setText(String.valueOf(hs.getDiemTB()));
        if (hs.getGioiTinh().equalsIgnoreCase("nam")) {
            radioNam.setSelected(true);
        } else {
            radioNu.setSelected(true);
        }
        lblHienTai.setText(String.valueOf(index + 1));

    }
