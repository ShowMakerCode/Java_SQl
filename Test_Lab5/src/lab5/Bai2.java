    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        if (txtTen.getText().equals("") | txtDiemTb.getText().equals("") | txtMAHS.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "VUi Long Khong De Trong Thong Tin");
            return;
        }
        try {
            String mahs = txtMAHS.getText();
            String tenhs = txtTen.getText();
            double diem = Double.parseDouble(txtDiemTb.getText());
            String GioiTinh = radioNam.isSelected() ? "Nam" : "Nữ";

            listHocSinh.add(new HocSinh(mahs, tenhs, GioiTinh, diem));

            String sql = "insert into HOCSINH(MaHS,TenHS,GioiTinh,DiemTB)\n"
                    + "values(?,?,?,?)";
            PreparedStatement pstm = cn.prepareStatement(sql);
            pstm.setString(1, mahs);
            pstm.setString(2, tenhs);
            pstm.setString(3, GioiTinh);
            pstm.setDouble(4, diem);

            if (pstm.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "thêm thành công");
            }
            index = listHocSinh.size() - 1;
            showDetail();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "lỗi add");
        }
    }//GEN-LAST:event_btnADDActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            String mahs = txtMAHS.getText();
            String tenhs = txtTen.getText();
            double diem = Double.parseDouble(txtDiemTb.getText());
            String GioiTinh = radioNam.isSelected() ? "Nam" : "Nữ";
            listHocSinh.set(index, new HocSinh(mahs, tenhs, GioiTinh, diem));
            String sql = "update HOCSINH\n"
                    + "set TenHS =?, GioiTinh=?, DiemTB=?\n"
                    + "where MaHS=?";
            PreparedStatement pstm = cn.prepareStatement(sql);
            pstm.setString(1, tenhs);
            pstm.setString(2, GioiTinh);
            pstm.setDouble(3, diem);
            pstm.setString(4, mahs);
            if (pstm.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "thêm thành công");
            }
            showDetail();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "lỗi add");
        }
    }//GEN-LAST:event_btnEditActionPerformed

